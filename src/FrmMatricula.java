    private void mostrarDatos(){
        int cantidadEstudiantes = GestionColegio.listaEstudiantes.size();
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) jComboBoxEstudiante.getModel();
        jComboBoxEstudiante.setModel(modelo);
        for(int i=0;i<cantidadEstudiantes;i++){
            modelo.addElement(GestionColegio.listaEstudiantes.get(i));
        }
        
        int cantidadCursos = GestionColegio.listaCursos.size();
        DefaultComboBoxModel modelo2= (DefaultComboBoxModel) jComboBoxCurso.getModel();
        jComboBoxCurso.setModel(modelo2);
        for(int i=0;i<cantidadCursos;i++){
            modelo2.addElement(GestionColegio.listaCursos.get(i));
        }
    }

