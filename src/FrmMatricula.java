    private void jButtonAgregarMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarMatriculaActionPerformed
        // TODO add your handling code here:
        Curso curso = (Curso)jComboBoxCurso.getSelectedItem();
        Estudiante estudiante = (Estudiante) jComboBoxEstudiante.getSelectedItem();             
        Matricula matricula = new Matricula(curso, estudiante);
        //primero se pregunta si ya est√° matriculado  
        if (!GestionColegio.estaMatriculado(matricula)){
            GestionColegio.agregarMatricula(matricula);
            JOptionPane.showMessageDialog(null, "Matricula Agregada");  
        }else{
            JOptionPane.showMessageDialog(null, "Ya existe el mismo estudiante matriculado en el mismo curso");  
        }
    }//GEN-LAST:event_jButtonAgregarMatriculaActionPerformed

